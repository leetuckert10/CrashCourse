# Favorite Fruit
# Demonstrates using individual 'if' statements.

favorite_fruit = ['strawberries','oranges','apples']

if 'bananas' in favorite_fruit:
    print("You really like bananas!")
if 'strawberries' in favorite_fruit:
    print("You really like strawberries!")
if 'oranges' in favorite_fruit:
    print("You really like oranges!")
if 'blueberries' in favorite_fruit:
    print("You really like blueberries!")
if 'apples' in favorite_fruit:
    print("You really like apples!")
