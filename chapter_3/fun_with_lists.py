bicycles = ['trek', 'cannondale', 'redline', 'specialized']
print(bicycles[0].title())		# like most languages, zero based indexing

# Special syntax for accessing the last element in a list
print(bicycles[-1].title())		# last element
print(bicycles[-2].title())		# next to last element

print(f"I have a {bicycles[0].title()} bicycle in my garage.")
